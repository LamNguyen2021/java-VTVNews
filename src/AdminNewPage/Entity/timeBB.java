package AdminNewPage.Entity;

public class timeBB {
	private String time;
	private baibao bb;
	
	public timeBB() {
		super();
	}

	public timeBB(String time, baibao bb) {
		super();
		this.time = time;
		this.bb = bb;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public baibao getBb() {
		return bb;
	}

	public void setBb(baibao bb) {
		this.bb = bb;
	}
	
	
}
